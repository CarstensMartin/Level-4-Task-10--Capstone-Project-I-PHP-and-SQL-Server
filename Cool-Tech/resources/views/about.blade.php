        <h2>What is an About Us page?</h2>
        <p>
            An About Us page is a home for your founding story, a place to show off your business wins, and a sales page that answers the most pressing question new customers have about your business, and why they should buy your products or service.<br><br>
            The problem with most About Us pages is that they’re an afterthought—a link buried at the bottom of a site that leads to a few hastily written paragraphs about a company.<br><br>
            What an About Us page should be is a goal-oriented sales page, one that focuses on highlighting the biggest selling points of your story and brand at the top of the page, making a strong first impression on curious customers<br><br>
            In Shopify’s customer trust research, we found that shoppers navigate to an About Us page to learn more about the brand and the people behind the products. Your About Page should address those two curiosities shoppers have to help with decision making. <br><br>
            Shoppers are also interested in a company’s mission. They’ll use the About Us page to determine if they share core values with the business and to decide if they want to shop with the business or not. <br><br>
            In contrast to a landing page, your About Us page is the ideal place to accommodate a number of objectives:
        </p>

        <ul>
            <li>Communicate the story of your business and why you started it.</li>
            <li>Describe the customers or the cause that your business serves.</li>
            <li>Explain your business model or how your products are made.</li>
            <li>Put a face to your business, featuring the founders or the people on your team</li>
            <li>Incorporate persuasive content (e.g., an explainer video, data visualizations, links to blog posts) that might otherwise clutter your homepage.</li>
        </ul>
